#The radius of circle is 45cm, if pie =3.142. 
# ,Write a python code that will calculate the area of the circle 
# and the circumference of the circle. Add the area and the circumference 
# together and print it out
r=45
pie=3.142
circumference=2*pie*r
print("circumference")
area=pie*r*r
print("area")
d=(circumference+area)
print(d)
